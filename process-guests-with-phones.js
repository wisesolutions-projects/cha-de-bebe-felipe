// Extract apelido from name
function extractApelido(nome) {
  // Check for parentheses nickname
  const match = nome.match(/\(([^)]+)\)/);
  if (match) return match[1];

  // Otherwise use first word
  const words = nome.split(/[\s,]+/);
  return words[0];
}

const guests = lines.map(line => {
  // Split by multiple spaces (4 or more)
  const parts = line.split(/\s{4,}/);
  if (parts.length < 4) {
    console.error('Invalid line:', line);
    return null;
  }
  const nome = parts[0].trim();
  const prefixo = parts[1].trim();
  const telefone = parts[2].trim();
  const item = parts[3].trim();
  const apelido = extractApelido(nome);
  const telefoneCompleto = `${prefixo} ${telefone}`;

  return { nome, apelido, telefone: telefoneCompleto, item };
}).filter(g => g !== null);

